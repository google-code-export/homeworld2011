namespace PlagueEngine.EventsSystem
{

    /********************************************************************************/
    /// Events Sniffer Component
    /********************************************************************************/
    class EventsSnifferComponent : EventsSniffer
    {

        /****************************************************************************/
        /// Delegates
        /****************************************************************************/
        public delegate void OnEvent(EventsSender sender,IEventsReceiver receiver,EventArgs e);
        /****************************************************************************/


        /****************************************************************************/
        /// Fields
        /****************************************************************************/
        private OnEvent onSniffedEvent = null;
        /****************************************************************************/


        /****************************************************************************/
        /// Set On Sniffed Event Delegate
        /****************************************************************************/
        public void SetOnSniffedEvent(OnEvent method)
        {
            onSniffedEvent = method;
        }
        /****************************************************************************/


        /****************************************************************************/
        /// On Sniffed Event
        /****************************************************************************/
        public override void OnSniffedEvent(EventsSender sender, IEventsReceiver receiver, EventArgs e)
        {
            onSniffedEvent(sender, receiver, e);
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Release Me
        /****************************************************************************/
        public override void ReleaseMe()
        {
            base.ReleaseMe();
        }
        /****************************************************************************/

    }
    /********************************************************************************/

}
