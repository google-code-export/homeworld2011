using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Nuclex.UserInterface.Controls;
using Nuclex.UserInterface;

/************************************************************************************/
/// PlagueEngine.GUI.Components
/************************************************************************************/
namespace PlagueEngine.GUI.Components
{
    /********************************************************************************/
    /// GUI Label Component
    /********************************************************************************/
    class LabelComponent : GUIComponent
    {
        /****************************************************************************/
        /// Fields
        /****************************************************************************/
        public LabelControl Control { get; private set; }
        /****************************************************************************/
        
        /****************************************************************************/
        /// Constructor
        /****************************************************************************/
        public LabelComponent(String text, UniRectangle bounds)
            : base()
        {
            Control = new LabelControl();
            Control.Bounds = bounds;
            Control.Text = text;                  
        }
        /****************************************************************************/
                        

        /****************************************************************************/
        /// register
        /****************************************************************************/
        public override void Register()
        {
            gui.Manager.Screen.Desktop.Children.Add(this.Control);
        }
        /****************************************************************************/
        

        /****************************************************************************/
        /// Release Me 
        /****************************************************************************/
        public override void ReleaseMe()
        {
            Control.Parent.Children.Remove(Control);
            base.ReleaseMe();
        }
        /****************************************************************************/
    }
}
