    /********************************************************************************/
    /// PlagueEngineCollisionSkin
    /********************************************************************************/
    class PlagueEngineCollisionSkin : CollisionSkin
    {

        /****************************************************************************/
        /// Fields
        /****************************************************************************/
        private GameObjectInstance gameObject = null;
        /****************************************************************************/


        /****************************************************************************/
        /// Constructor (1)
        /****************************************************************************/
        public PlagueEngineCollisionSkin(GameObjectInstance gameObject,Body body)
            : base(body)
        {
            this.gameObject = gameObject;
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Constructor (2)
        /****************************************************************************/
        public PlagueEngineCollisionSkin(GameObjectInstance gameObject) 
            : base()
        {
            this.gameObject = gameObject;
        }
        /****************************************************************************/


        /****************************************************************************/
        /// GameObject
        /****************************************************************************/
        public GameObjectInstance GameObject
        {
            get 
            { 
                return gameObject; 
            }
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Release
        /****************************************************************************/
        public void Release()
        {
            gameObject = null;
        }
        /****************************************************************************/

    }
