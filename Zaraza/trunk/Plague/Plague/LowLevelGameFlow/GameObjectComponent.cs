using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

using PlagueEngine.EventsSystem;
/************************************************************************************/
/// PlagueEngine.LowLevelGameFlow
/************************************************************************************/
namespace PlagueEngine.LowLevelGameFlow
{

    /********************************************************************************/
    ///  Component
    /********************************************************************************/
    abstract class GameObjectComponent : EventsSender, IEventsReceiver
    {

        /****************************************************************************/
        /// Fields
        /****************************************************************************/
        protected GameObjectInstance gameObject = null;
        /****************************************************************************/


        /****************************************************************************/
        /// Constructor
        /****************************************************************************/
        public GameObjectComponent(GameObjectInstance gameObject)
        {
            this.gameObject = gameObject;
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Game Object
        /****************************************************************************/
        public GameObjectInstance GameObject
        {
            get
            {
                return gameObject;
            }
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Release Me
        /****************************************************************************/
        public abstract void ReleaseMe();
        /****************************************************************************/



        /****************************************************************************/
        /// Dispose
        /****************************************************************************/
        public void Dispose()
        {
            gameObject.Dispose();
        }
        /****************************************************************************/


        /****************************************************************************/
        /// IsDisposed
        /****************************************************************************/
        public bool IsDisposed()
        {
            return gameObject.IsDisposed();
        }
        /****************************************************************************/


        /****************************************************************************/
        /// On Event
        /****************************************************************************/
        public virtual void OnEvent(EventsSender sender, EventArgs e)
        {

        }
        /****************************************************************************/


        /****************************************************************************/
        /// Get String
        /****************************************************************************/
        public override String ToString()
        {
            StringBuilder builder = new StringBuilder();
            builder.Append(gameObject.GetType().Name);
            builder.Append(" - ");
            builder.Append(gameObject.ID);

            return builder.ToString();
        }
        /****************************************************************************/

    }
    /********************************************************************************/

}
/************************************************************************************/