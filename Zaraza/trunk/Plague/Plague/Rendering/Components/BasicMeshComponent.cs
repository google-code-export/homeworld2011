using System;
using System.Collections.Generic;
using System.Text;
using Microsoft.Xna.Framework.Graphics;
using Microsoft.Xna.Framework;

using PlagueEngine.LowLevelGameFlow;


/************************************************************************************/
/// PlagueEngine.Rendering.Components
/************************************************************************************/
namespace PlagueEngine.Rendering.Components
{

    /********************************************************************************/
    /// Basic Mesh Component
    /********************************************************************************/
    class BasicMeshComponent : RenderableComponent
    {
        
        /****************************************************************************/
        /// Fields
        /****************************************************************************/
        private Model  model     = null;
        private String modelName = String.Empty;             
        /****************************************************************************/


        /****************************************************************************/
        /// BasicMeshComponent
        /****************************************************************************/
        public BasicMeshComponent(GameObjectInstance gameObject,Renderer renderer,Model model,String name) : base(gameObject,renderer,null)
        {
            this.model     = model;
            this.modelName = name;
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Model
        /****************************************************************************/
        public String Model
        {
            get
            {
                return modelName;
            }
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Release Me
        /****************************************************************************/
        public override void ReleaseMe()
        {
            base.ReleaseMe();
        }
        /****************************************************************************/


        /****************************************************************************/
        /// Draw
        /****************************************************************************/
        public override void Draw()
        {
            model.Draw(gameObject.World, renderer.CurrentCamera.View, renderer.CurrentCamera.Projection);                
        }
        /****************************************************************************/

    }
    /********************************************************************************/

}
